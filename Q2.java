package math1;

public class Q2 {

	public static void main(String[] args) {
		
		int a =10;
		int b =20;
		int temp=0;
		 System.out.println("before swap");
		 System.out.println("first "+ a);
		 System.out.println("second" + b);
		 
		 temp=a;
		 a=b;
		 b=temp;
		 System.out.println("after swap");
		 System.out.println("first "+ a);
		 System.out.println("second" + b);
	}

}
